// Helper function to check if assets are expired (older than 7 days)
export function checkAssetExpiry(urlOrObj: string | Record<string, any>, createdDate?: Date): boolean {
  const created = createdDate || new Date('2025-01-15'); // Assumed creation date
  const now = new Date();
  const daysSince = (now.getTime() - created.getTime()) / (1000 * 60 * 60 * 24);
  return daysSince > 7;
}

